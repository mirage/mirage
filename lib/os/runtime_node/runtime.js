function caml_blit_string(s1, i1, s2, i2, len) {
  if (len === 0) return;
  if (i1 < 0 || i1 + len > s1.length || i2 < 0 || i2 + len > s2.length) {
    caml_invalid_argument ("String.blit");
    return;
  };
  var chunk = s1.toString('hex', i1, i1+len);
  var w = s2.write(chunk, i2, len, 'hex');
  return;
}
